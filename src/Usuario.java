public class Usuario {

    private int id;
    private String nome;
    private String email;

    // GET ID
    public int getId() {
        return id;
    }

    // SET ID
    public void setId(int id) {
        this.id = id;
    }

    // GET NOME
    public String getNome() {
        return nome;
    }

    // SET NOME
    public void setNome(String nome) {
        this.nome = nome;
    }

    // GET EMAIL
    public String getEmail() {
        return email;
    }

    // SET EMAIL
    public void setEmail(String email) {
        this.email = email;
    }
}
